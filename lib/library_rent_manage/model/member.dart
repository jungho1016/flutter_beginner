class Member {
  int memberId;
  String name;
  String id;
  DateTime joinDay;
  String address;
  String phoneNumber;
  DateTime birthday;

  Member({
    required this.memberId,
    required this.id,
    required this.joinDay,
    required this.name,
    required this.address,
    required this.phoneNumber,
    required this.birthday,
  });
}
